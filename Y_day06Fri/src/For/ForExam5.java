public class ForExam5 {

	public static void main(String[] args) {

		for(int i=0; i<10; i++) {
			System.out.print(i+"\t");
		}
		
		System.out.println();
		for(int i=0; i<10; i+=3) {
			System.out.print(i+"\t");
		}

		System.out.println();
		for(int i=0; i<10; i+=3) {
			System.out.print(i+"\t");
		}

		System.out.println();
		for(int i=0; i<10; i+=3) {
			System.out.print(i+"\t");
		}

	}
}
	
