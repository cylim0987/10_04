package For;

public class ForExam4gugu2 {
	public static void main(String[] args) {
		int i;
		for(i=1; i<=9; i++) {
			for(int j=1; j<=9; j++) {
				System.out.print(j + "*" + i + "=" + j*i+"\t"); // i와 j의 위치 바꿔보기.
			}
		System.out.println();
		}
		System.out.println("i=" +i);
	}
}
	
